function showD() {
  document.getElementById("code").style.display = "block"
  document.getElementById("showD").style.display = "none"
  document.getElementById("hideD").style.display = "inline-block"
  document.getElementById("brToCode").style.display = "block"
}
function hideD() {
  document.getElementById("code").style.display = "none"
  document.getElementById("hideD").style.display = "none"
  document.getElementById("showD").style.display = "inline-block"
  document.getElementById("brToCode").style.display = "none"
}
